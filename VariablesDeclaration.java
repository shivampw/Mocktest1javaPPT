public class VariablesDeclaration {

    public static void main(String[] args) {

        // declare an integer variable
        int integerVariable = 10;

        // declare a double variable
        double doubleVariable = 10.5;

        // declare a string variable
        String stringVariable = "Hello, world!";

        // print the values of the variables
        System.out.println("integerVariable: " + integerVariable);
        System.out.println("doubleVariable: " + doubleVariable);
        System.out.println("stringVariable: " + stringVariable);
    }
}

